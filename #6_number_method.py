import math

# some useful number functions

pi = 3.14
x = 1
y = 2
z = 3

print(round(pi)) # give a full integer
print(math.ceil(pi)) # give a upper round number
print(math.floor(pi)) # give a lower round number
print(abs(-2.55))  # give a positive interger always
print(math.sqrt(36)) # give a square root of an integer
print(math.pow(2,3)) # give the power of a number, here base=2, power=3
print(max(x,y,z)) # give the maximum number
print(min(x,y,z)) # give the minimum number
print(math.gcd(20,30,40)) # give the gcd of given values
print(math.factorial(45)) # give the factorial of a given number